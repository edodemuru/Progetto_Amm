CREATE TABLE utente(
    nome VARCHAR(32),
    cognome VARCHAR(32),
    urlFotoProfilo VARCHAR(1024),
    frasePres VARCHAR(256),
    dataNasc VARCHAR(256),
    username VARCHAR(256) NOT NULL,
    password VARCHAR(256) NOT NULL,
    idUtente INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
);

CREATE TABLE gruppo(
    urlFotoGruppo VARCHAR(1024),
    interesse VARCHAR(1024),
    nome VARCHAR(1024),
    idGruppo INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    idAmministratore INTEGER,
    FOREIGN KEY (idAmministratore) REFERENCES utente(id)

);

CREATE TABLE PostType(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR

);

CREATE TABLE post(
    idPostType INTEGER,
    FOREIGN KEY (idPostType) REFERENCES PostType(id),
    content VARCHAR(1024),
    text VARCHAR(1024),
    idPost INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    idMittente INTEGER,
    FOREIGN KEY(idMittente) REFERENCES utente(id),
    idDestinatarioUtente INTEGER,
    FOREIGN KEY(idDestinatarioUtente) REFERENCES utente(id),
    idDestinatarioGruppo INTEGER,
    FOREIGN KEY(idDestinatarioGruppo) REFERENCES gruppo(id)  
);

CREATE TABLE amicizia(
    follower INTEGER,
    FOREIGN KEY(follower) REFERENCES utente(id),
    followed INTEGER,
    FOREIGN KEY(followed) REFERENCES utente(id),
    PRIMARY KEY (follower,followed)
);

CREATE TABLE partecipazione gruppo(
    idUtente INTEGER,
    idGruppo INTEGER,
    FOREIGN KEY(idUtente) REFERENCES utente(id),
    FOREIGN KEY(idGruppo) REFERENCES gruppo(id)
);

